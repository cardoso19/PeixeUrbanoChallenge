import UIKit

extension UIColor {
    
    /// A color object that is the main blue of the company Peixe Urbano.
    class var mainBlue: UIColor? { return UIColor(named: "MainBlue") }
    
    /// A color object that is the main orange of the company Peixe Urbano.
    class var mainOrange: UIColor? { return UIColor(named: "MainOrange") }
    
    /// A color object that is the black for text.
    class var textBlack: UIColor? { return UIColor(named: "TextBlack") }
    
    /// A color object that is the gray for text.
    class var textGray: UIColor? { return UIColor(named: "TextGray") }
    
    /// A color object that is the light gray for text.
    class var textLightGray: UIColor? { return UIColor(named: "TextLightGray") }
}
